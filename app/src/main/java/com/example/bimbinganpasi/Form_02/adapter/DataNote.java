package com.example.bimbinganpasi.Form_02.adapter;


public class DataNote {
    String matkul_list;
    String sks_list;

    public DataNote(String matkul_list, String sks_list) {
        this.matkul_list = matkul_list;
        this.sks_list = sks_list;
    }

    public String getMatkul_list() {
        return matkul_list;
    }

    public String getSks_list() {
        return sks_list;
    }
}

